// Matching wit hOption<T>
fn plus_one(x: Option<i32>) -> Option<i32> {
    match x {
        None => None,           // if this were not here, we would get a compiler error
        Some(i) => Some(i + 1),
    }
}

    
fn main() {
    let five = Some(5);
    let six = plus_one(five);
    let none = plus_one(None);

    let some_u8_value = 0u8;    // the _ placeholder allows us to only handle the "arms"
    match some_u8_value {       // we want/need to, and it tells Rust we can ignore the rest
        1 => println!("one"),
        3 => println!("three"),
        5 => println!("five"),
        7 => println!("seven"),
        _ => (),
    }
}